#Um cinema possui 80 lugares e o valor do ingresso é R$ 20,00. Considerando que são 03 (três) sessões em
#um dia de lotação. Faça um algoritmo para calcular e mostrar o total de faturamento do cinema no dia,
#na semana (07 dias) e no mês (30 dias):

dia = (80*20)*3
semana = dia*7
mes = dia*30

print("O valor arrecadado no dia é de:", dia)
print("O valor arrecadado na semana é de:", semana)
print("O valor arrecadado no mês:", mes)